    private void search() {
        try {
            ResultSet rs = DB_connection.search("select * from vehicle where Engine_No = '" + engno.getText() + "'");

            while (rs.next()) {
                chasno.setText(rs.getString("Chasis_No"));
                engcap.setText(rs.getString("Eng_Capacity"));
                model.setText(rs.getString("Model"));
                type1.setText(rs.getString("Type"));
                col.setText(rs.getString("Color"));
                manu.setText(rs.getString("Manufacture"));
                jComboBox1.setSelectedItem(rs.getString("Year"));
                cost.setText(rs.getString("Cost"));
                sellpri.setText(rs.getString("Selling_Price"));

                String s2;
                String s = rs.getString("Status");

                if ("1".equals(s)) {
                    s2 = "On_sale";
                } else if ("0".equals(s)) {
                    s2 = "Sold";
                } else {
                    s2 = "Recerved";
                }

                jComboBox2.setSelectedItem(s2);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void setYear() {

        d = new Date();
        sdf = new SimpleDateFormat("YYYY/MM/dd");

        String s = sdf.format(d);
        int s1 = Integer.parseInt(s.substring(0, 4));
        int s2 = s1 - 30;

        while (s1 >= s2) {
            String p = Integer.toString(s1);
            jComboBox1.addItem(p);

            s1--;
        }

    }

