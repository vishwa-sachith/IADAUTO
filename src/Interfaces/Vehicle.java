    private void loadsup() {

        try {

            jTable1.removeAll();
            jTable1.repaint();
            jTable1.revalidate();

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.getDataVector().removeAllElements();
            ResultSet sr = DB_connection.search("select * from vehicle");

            while (sr.next()) {

                String s2;
                String s = sr.getString("Status");

                if ("1".equals(s)) {
                    s2 = "On_sale";
                } else if ("0".equals(s)) {
                    s2 = "Sold";
                } else {
                    s2 = "Recerved";
                }

                Vector v = new Vector();

                v.add(sr.getString("Engine_No"));
                v.add(sr.getString("Chasis_No"));
                v.add(sr.getString("Eng_Capacity"));
                v.add(sr.getString("Model"));
                v.add(sr.getString("Type"));
                v.add(sr.getString("Color"));
                v.add(sr.getString("Manufacture"));
                v.add(sr.getString("Year"));
                v.add(sr.getString("Date_Of_Baught"));
                v.add(sr.getString("Cost"));
                v.add(sr.getString("Selling_Price"));
                v.add(s2);

                dtm.addRow(v);
            }
            engno.grabFocus();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

